export function getPlantsForProfile(profile) {
  if (!profile) return [];
  const pools = profile.plantPools;
  const matching = plants.filter(plant =>
    plant.pools.some(pool => pools.includes(pool))
  );
  const shuffled = [...matching].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, 5);
}
